"""
Pide dos numero y pide todas las operaciones aritmeticologicas
y mostrarlo por pantalla
"""

numero1 = input("Numero 1: ")
numero2 = input("Numero 2: ")

print("### CALCULADORA ###")
print(f"Suma: {numero1} + {numero2} = {int(numero1) + int(numero2)}")
print(f"Resta: {numero1} - {numero2} = {int(numero1) - int(numero2)}")
print(f"Multiplicación: {numero1} x {numero2} = {int(numero1) * int(numero2)}")
print(f"División: {numero1} / {numero2} = {int(numero1) / int(numero2)}")